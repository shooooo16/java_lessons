  public class MyApp4 {
    public static void main(String[] args) {
      //演算
      //+ - * / %
      //++ --
      int i;
      i = 10/3;
      System.out.println(i); //小数点切捨てで3

      i = 10%3;
      System.out.println(i);

      int x = 5;
      x++;
      System.out.println(x);
      x--;
      System.out.println(x);

      x += 12;
      System.out.println(x); //17

      String s;
      s = "hello" + " world";
      System.out.println(s);

    }
  }
